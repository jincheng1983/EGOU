// 自动选择智能体
export function autoSelectAgent(userMessage, availableAgents) {
  if (!userMessage) return null
  const msg = userMessage.toLowerCase()
  for (const agent of availableAgents) {
    if (!agent.autoSelect) continue
    for (const keyword of agent.autoSelect) {
      if (msg.includes(keyword.toLowerCase())) {
        return agent.id
      }
    }
  }
  return null
}

